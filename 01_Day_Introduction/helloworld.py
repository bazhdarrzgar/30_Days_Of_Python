print('========================== different type of operator ========================== ')

# using   ( print() )   method to print anything you want 
print(2 + 3)   # addition (+) # 5
print(3 - 1)   # subtraction (-) # 2
print(2 * 3)   # multiplication (*) # 6
print(3 / 2)   # division (/) # 1.5
print(3 // 2)  # Floor division operator (//) # 1
print(3 ** 2)  # exponential (**) # 9
print(3 % 2)   # modulus (%) # 1

print('========================== type() ========================== ')
# Checking data types using   ( type() )   method
print(type(10))                    # Int # <class 'int'>
print(type(3.14))                  # Float # <class 'float'>
print(type(1 + 3j))                # Complex  (means character english and number is use here) # <class 'complex'>
print(type('Asabeneh'))            # String # <class 'str'>
print(type([1, 2, 3]))             # List # <class 'list'>
print(type({'name': 'Asabeneh'}))  # Dictionary # <class 'dict'>
print(type({9.8, 3.14, 2.7}))      # Set # <class 'set'>
print(type((9.8, 3.14, 2.7)))      # Tuple # <class 'tuple'>

